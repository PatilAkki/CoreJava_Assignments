
public class StartwithEndWith {

	public static void main(String[] args) {
		String s = "Abcd";
		if(s.startsWith("A"))
		{
			System.out.println("string starts with 'A'");
		}
		else
		{
			System.out.println("string does not start with 'A'");
		}
		
		if(s.endsWith("t"))
		{
			System.out.println("string end with 't'");
		}
		else
		{
			System.out.println("string does not ends with 't'");
		}
		

	}

}
